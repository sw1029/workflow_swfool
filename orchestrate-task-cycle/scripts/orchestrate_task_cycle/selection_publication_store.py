TRANSACTION_ID = re.compile(r"^selection-[0-9a-f]{64}$")
SHA256 = re.compile(r"^[0-9a-f]{64}$")


def _canonical_json(value: Any) -> bytes:
    rendered = json.dumps(
        value, ensure_ascii=False, sort_keys=True, separators=(",", ":")
    )
    return (rendered + "\n").encode("utf-8")


def _display_json(value: Any) -> bytes:
    rendered = json.dumps(
        value, ensure_ascii=False, indent=2, sort_keys=True
    )
    return (rendered + "\n").encode("utf-8")


def _sha256_bytes(payload: bytes) -> str:
    return hashlib.sha256(payload).hexdigest()


def _bounded_payload(payload: bytes, max_bytes: int, label: str) -> bytes:
    """Reject generated bytes before any store path or directory is touched."""

    if len(payload) > max_bytes:
        raise ValueError(f"{label} exceeds {max_bytes // 1024} KiB")
    return payload


def _sha256_file(path: Path) -> str | None:
    if not path.is_file() or path.is_symlink():
        return None
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def _bounded_file_sha256(path: Path, expected_size: int, label: str) -> str | None:
    """Hash one exact-size regular leaf without following a leaf symlink."""

    try:
        before = path.lstat()
    except FileNotFoundError:
        return None
    if (
        not stat.S_ISREG(before.st_mode)
        or before.st_size != expected_size
        or expected_size < 0
    ):
        raise ValueError(f"{label} conflicts with immutable transaction evidence")
    flags = os.O_RDONLY | getattr(os, "O_NOFOLLOW", 0) | getattr(os, "O_CLOEXEC", 0)
    try:
        descriptor = os.open(path, flags)
    except OSError as exc:
        raise ValueError(
            f"{label} conflicts with immutable transaction evidence"
        ) from exc
    try:
        opened = os.fstat(descriptor)
        if (
            not stat.S_ISREG(opened.st_mode)
            or (opened.st_dev, opened.st_ino, opened.st_size)
            != (before.st_dev, before.st_ino, expected_size)
        ):
            raise ValueError(
                f"{label} conflicts with immutable transaction evidence"
            )
        digest = hashlib.sha256()
        total = 0
        while total <= expected_size:
            chunk = os.read(descriptor, min(1024 * 1024, expected_size + 1 - total))
            if not chunk:
                break
            total += len(chunk)
            digest.update(chunk)
        after = os.fstat(descriptor)
        try:
            current = path.lstat()
        except OSError as exc:
            raise ValueError(
                f"{label} conflicts with immutable transaction evidence"
            ) from exc
        if (
            total != expected_size
            or _file_signature(opened) != _file_signature(after)
            or (current.st_dev, current.st_ino) != (opened.st_dev, opened.st_ino)
        ):
            raise ValueError(
                f"{label} conflicts with immutable transaction evidence"
            )
        return digest.hexdigest()
    finally:
        os.close(descriptor)


def _file_signature(value: os.stat_result) -> tuple[int, int, int, int, int, int]:
    return (
        value.st_dev,
        value.st_ino,
        value.st_mode,
        value.st_size,
        value.st_mtime_ns,
        value.st_ctime_ns,
    )


def _fsync_dir(path: Path) -> None:
    descriptor = os.open(path, os.O_RDONLY)
    try:
        os.fsync(descriptor)
    finally:
        os.close(descriptor)


def _selection_store_write(
    path: Path, producer_capability: object | None
) -> contextlib.AbstractContextManager[None]:
    # Classify lexically: resolving a symlinked store first erases its protected
    # path markers and would bypass the capability gate.
    lexical = Path(os.path.abspath(os.fspath(path)))
    parts = lexical.parts
    protected = any(
        parts[index : index + 2] == (".task", "selection_publication")
        for index in range(len(parts) - 1)
    )
    if not protected:
        return contextlib.nullcontext()
    _require_selection_publication_producer(producer_capability)
    store_index = next(
        index
        for index in range(len(parts) - 1)
        if parts[index : index + 2] == (".task", "selection_publication")
    )
    root = Path(*parts[:store_index])
    from .selection_publication_reference_barrier import (
        registered_producer_barrier,
    )

    return registered_producer_barrier(
        root, producer_capability=producer_capability
    )


def _atomic_write(
    path: Path, payload: bytes, *, producer_capability: object | None = None
) -> None:
    with _selection_store_write(path, producer_capability):
        _atomic_write_unlocked(path, payload)


def _atomic_write_unlocked(path: Path, payload: bytes) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    descriptor, temporary_value = tempfile.mkstemp(
        prefix=f".{path.name}.", suffix=".tmp", dir=path.parent
    )
    temporary = Path(temporary_value)
    try:
        with os.fdopen(descriptor, "wb") as handle:
            handle.write(payload)
            handle.flush()
            os.fsync(handle.fileno())
        os.replace(temporary, path)
        _fsync_dir(path.parent)
    finally:
        temporary.unlink(missing_ok=True)


def _write_once_with_status(
    path: Path, payload: bytes, label: str, *,
    producer_capability: object | None = None
) -> tuple[str, bool]:
    from .selection_publication_store_immutable import (
        _verify_exact,
        _write_once_unlocked_with_status,
    )

    write_context = _selection_store_write(path, producer_capability)
    if path.exists() or path.is_symlink():
        _verify_exact(path, payload, _sha256_bytes(payload), label)
    with write_context:
        return _write_once_unlocked_with_status(path, payload, label)


def _write_once(
    path: Path, payload: bytes, label: str, *,
    producer_capability: object | None = None
) -> str:
    return _write_once_with_status(
        path, payload, label, producer_capability=producer_capability
    )[0]


def _safe_directory(path: Path, label: str) -> None:
    if path.is_symlink():
        raise ValueError(f"{label} cannot be a symlink")
    if path.exists() and not path.is_dir():
        raise ValueError(f"{label} must be a directory")


def _safe_regular_file(path: Path, label: str) -> None:
    if path.is_symlink():
        raise ValueError(f"{label} cannot be a symlink")
    if path.exists() and not stat.S_ISREG(path.lstat().st_mode):
        raise ValueError(f"{label} must be a regular file or absent")


def _store_root(root: Path) -> Path:
    root = root.resolve(strict=True)
    task_root = root / ".task"
    store = task_root / "selection_publication"
    _safe_directory(task_root, "selection-publication .task root")
    _safe_directory(store, "selection-publication store root")
    try:
        store.resolve(strict=False).relative_to(root)
    except ValueError as exc:
        raise ValueError("selection-publication store escapes the workspace") from exc
    return store


def _safe_store_directory(root: Path, relative_parts: tuple[str, ...]) -> Path:
    current = _store_root(root)
    for part in relative_parts:
        current /= part
        _safe_directory(current, "selection-publication store component")
    return current


def _prepare_path(root: Path, transaction_id: str) -> Path:
    if not TRANSACTION_ID.fullmatch(transaction_id):
        raise ValueError("invalid selection-publication transaction id")
    directory = _safe_store_directory(root, ("transactions", transaction_id))
    path = directory / "prepare.json"
    _safe_regular_file(path, "selection-publication prepare journal")
    return path


def _transactions_root(root: Path) -> Path:
    return _safe_store_directory(root, ("transactions",))


