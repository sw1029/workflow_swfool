def _intent_index_path(root: Path, digest: str, name: str) -> Path:
    """Return one immutable O(1) intent lookup path.

    The digest is a directory component rather than caller-controlled path text,
    and both the directory chain and leaf retain the store's symlink guards.
    """

    if not SHA256.fullmatch(digest):
        raise ValueError("invalid selection-publication intent digest")
    if name not in {"prepare", "commit"}:
        raise ValueError("invalid selection-publication intent index kind")
    directory = _safe_store_directory(root, ("intents", "sha256", digest))
    path = directory / f"{name}.json"
    _safe_regular_file(path, f"selection-publication intent {name} index")
    return path


